std::string readFileIfExists(const std::string &path) {
  std::ifstream in(path);
  if (!in) {
    return {};
  }
  std::ostringstream ss;
  ss << in.rdbuf();
  return ss.str();
}

void replaceAll(std::string &text, const std::string &from,
                const std::string &to) {
  if (from.empty()) {
    return;
  }
  size_t pos = 0;
  while ((pos = text.find(from, pos)) != std::string::npos) {
    text.replace(pos, from.size(), to);
    pos += to.size();
  }
}

std::string xrootdTemplate() {
  return R"(# Generated by xjcd — JournalCache forwarding cache proxy
all.sitename JournalCache@{{hostname}}

port {{xroot_port}}
port tls {{https_port}}

xrd.protocol http:{{lib_dir}}/libXrdHttp.so

http.cert {{tls_cert}}
http.key {{tls_key}}

pss.origin =root,http,https
all.export /
ofs.osslib libXrdPss.so

http.header2cgi If-None-Match xrd.journalcache.if-none-match
http.header2cgi If-Modified-Since xrd.journalcache.if-modified-since

http.exthandler journalcache libXrdClJournalCacheHttpExt-{{plugin_suffix}}.so \
  {{http_ext_conf}}

xrootd.chksum max 2 md5 adler32 crc32 crc32c
)";
}

std::string httpExtTemplate() {
  return R"(# Generated by xjcd — JournalCache HTTP ext (forwarding proxy mode)
forwarding = 1

cache = {{journal}}/
flat = 0
basepath =

prefix = /
exclude = /static/

policy = {{policy_path}}
policy_poll = 2

# allow_origin / external_redirect: edit {{policy_path}} with xjc
)";
}

std::string clientPluginTemplate() {
  return R"(# Generated by xjcd — client plugins for the xrootd/PSS process
url = *
lib = libXrdClJournalCachePlugin-{{plugin_suffix}}.so
enable = true
cache = {{journal}}/
stats = 60
policy = {{policy_path}}
policy_poll = 2
multi_origin = 1

url = http*
lib = libXrdClHttp.so
enable = true

url = https*
lib = libXrdClHttp.so
enable = true
)";
}

std::string systemdEnvTemplate() {
  return R"(# Generated by xjcd — EnvironmentFile snippet for systemd
XRD_PLUGINCONFDIR={{client_plugin_dir}}
XRD_JOURNALCACHE_CACHE={{journal}}/
XRD_JOURNALCACHE_POLICY={{policy_path}}
)";
}

std::string systemdUnitHint(const XjcdState &state) {
  std::ostringstream out;
  out << "# Suggested systemd unit (install separately):\n"
      << "#   xrootd -c " << state.xrootdConfigPath() << " -R daemon\n"
      << "# EnvironmentFile=" << state.systemdEnvPath() << "\n";
  return out.str();
}

