std::string systemdUnitHint(const XjcdState &state) {
  std::ostringstream out;
  out << "# Install the generated units:\n"
      << "#   sudo install -m 0644 " << state.systemdUnitPath()
      << " /etc/systemd/system/\n"
      << "#   sudo install -m 0644 " << state.cleanerSystemdUnitPath()
      << " /etc/systemd/system/\n"
      << "#   sudo systemctl daemon-reload\n"
      << "#   sudo systemctl enable --now xjcd.service xjccleand.service\n";
  return out.str();
}

